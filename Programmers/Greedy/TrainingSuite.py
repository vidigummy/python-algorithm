def makeDict(lost, reserve, n):
    makingDict = dict()
    for i in range(1, n+1):
        makingDict[i] = 1
    for i in reserve:
        makingDict[i] = makingDict[i]+1
    for i in lost:
        makingDict[i] = makingDict[i]-1
    return makingDict


def solution(n, lost, reserve):
    answer = 0
    hashMap = makeDict(lost,reserve, n)
    totalTrainingSuite = 0
    for i in hashMap.keys():
        totalTrainingSuite += hashMap[i]
    if(totalTrainingSuite>=n):
        answer = n
    else:
        answer = totalTrainingSuite
    return answer

